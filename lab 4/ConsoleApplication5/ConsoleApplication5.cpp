#include <iostream>
#include <string>

using namespace std;

int main()
{
    // กำหนดเลขที่ต้องการทำตารางสูตรคูณ (เลขท้าย 2 หลัก คือ 02)
    int number = 2;

    cout << "***********************************\n";
    cout << "*** ตารางสูตรคูณแม่ " << number << " ***\n";
    cout << "***********************************\n";

    // ใช้ for loop เพื่อคำนวณและแสดงผลการคูณจาก 1 ถึง 12
    for (int i = 1; i <= 12; ++i)
    {
        // คำนวณผลลัพธ์
        int result = number * i;

        // แสดงผลในรูปแบบ: 2 x 1 = 2
        // การใช้ setw(2) ช่วยจัดรูปแบบให้สวยงาม (ต้อง include <iomanip> ด้วย หากต้องการใช้)
        // แต่ในตัวอย่างนี้ใช้แบบพื้นฐานก่อน
        cout << number << " x " << i << " = " << result << "\n";
    }

    return 0;
}